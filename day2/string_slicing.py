#py program to demonstarte string slicing

#creating a string
string1='GeeksForGeeks'
print('Initial String')
print(string1)

#print 3rd to 12th character
print('slicing character from 3rd to 12th:')
print(string1[3:12])

#print character b/w 3rd and 2nd last character
print('Slicing character b/w 3rd and 2nd last:')
print(string1[3:11])